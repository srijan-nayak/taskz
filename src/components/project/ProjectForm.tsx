"use client";

import {
  Field,
  FieldDescription,
  FieldError,
  FieldGroup,
  FieldLabel,
  FieldSet,
} from "@/components/ui/field";
import { Input } from "@/components/ui/input";
import { Button } from "@/components/ui/button";
import { ProjectFormState } from "@/lib/definitions/project";
import { useParams } from "next/navigation";

export default function ProjectForm({
  state,
  action,
  pending,
}: {
  state: ProjectFormState;
  action: (payload: FormData) => void;
  pending: boolean;
}) {
  const { orgId } = useParams<{ orgId: string }>();

  return (
    <form action={action}>
      <FieldSet>
        <FieldGroup>
          <Field hidden>
            <Input name="org-id" type="text" defaultValue={orgId} readOnly />
          </Field>
          <Field>
            <FieldLabel htmlFor="name">Name</FieldLabel>
            <Input
              id="name"
              name="name"
              type="text"
              placeholder="Todoz"
              defaultValue={state?.name || ""}
            />
            {state?.errors?.name && (
              <FieldError>{state.errors.name}</FieldError>
            )}
          </Field>
          <Field>
            <FieldLabel htmlFor="id">Project ID</FieldLabel>
            <FieldDescription>
              Add a short unique identifier, leave blank to have it
              autogenerated
            </FieldDescription>
            <Input
              id="id"
              name="id"
              type="text"
              placeholder="TODOZ"
              defaultValue={state?.id || ""}
            />
            {state?.errors?.id && <FieldError>{state.errors.id}</FieldError>}
          </Field>
        </FieldGroup>
        <FieldGroup>
          <Field>
            <Button
              type="submit"
              className="w-full cursor-pointer"
              disabled={pending}
            >
              Create Project
            </Button>
            {state?.message && <FieldError>{state.message}</FieldError>}
          </Field>
        </FieldGroup>
      </FieldSet>
    </form>
  );
}
